char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	while (dest[y])
		y++;
	while (src[i])
	{
		dest[i + y] = src[i];
		i++;
	}
	dest[i + y] = '\0';
	return (dest);
}
